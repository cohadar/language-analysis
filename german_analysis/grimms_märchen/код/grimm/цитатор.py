from grimm.токенизер import ТокенСпејс, ТокенСам, ТокенРеч
from grimm.токенизер import ТокенНаводник, ТокенЗатворениНаводник, ТокенОтворениНаводник
from grimm.токенизер import ТОКЕН_ЗА_ПОЧЕТАК, ТОКЕН_ЗА_КРАЈ
from grimm.словизер import Словизер
ОТВОРЕНИ = ТокенОтворениНаводник('„')
ЗАТВОРЕНИ = ТокенЗатворениНаводник('“')


def крај(т):
    return т == ТОКЕН_ЗА_КРАЈ


def неодређен_наводник(т):
    return type(т) == ТокенНаводник


def реч(т):
    return type(т) == ТокенРеч


def спејс(т):
    return type(т) == ТокенСпејс


class Цитатор:
    def __init__(бре, улаз):
        бре.улаз = улаз
        бре.излаз = []

    def __call__(бре):
        бре.и = Словизер(бре.улаз, почетак=ТОКЕН_ЗА_ПОЧЕТАК, крај=ТОКЕН_ЗА_КРАЈ)
        next(бре.и, ТОКЕН_ЗА_КРАЈ)
        while not крај(бре.и.тренутно):
            бре.цитирај()
        return бре.излаз

    def цитирај(бре):
        т = бре.и.тренутно
        if неодређен_наводник(т):
            бре.излаз.append(бре.наводник())
        else:
            бре.излаз.append(т)
        next(бре.и, ТОКЕН_ЗА_КРАЈ)

    def __str__(бре):
        return ''.join((т.текст for т in бре.излаз))

    def одреди_наводник(бре, прет, след):
        if прет.текст in [':']:
            return ОТВОРЕНИ
        elif прет.текст in ['.'] and реч(след):
            return ЗАТВОРЕНИ
        elif прет.текст in [' ', '\n', '', '-', '.'] and реч(след):
            return ОТВОРЕНИ
        elif прет.текст in [',', ';', '.', '!', '?'] and след.текст in [' ', '\n', '']:
            return ЗАТВОРЕНИ
        elif (реч(прет) or прет.текст in ['‹', '\'']) and след.текст in [' ', '\n', '', ';']:
            return ЗАТВОРЕНИ
        return None

    def наводник(бре):
        н = бре.одреди_наводник(бре.и.претходно, бре.и.следеће)
        if н:
            бре.излаз.append(н)
            return next(бре.и, ТОКЕН_ЗА_КРАЈ)
        if спејс(бре.и.претходно) and спејс(бре.и.следеће):
            бре.излаз.append(ЗАТВОРЕНИ)
            return next(бре.и, ТОКЕН_ЗА_КРАЈ)
        if бре.и.тренутно.текст == "'":
            бре.излаз.append(бре.и.тренутно)
            return next(бре.и, ТОКЕН_ЗА_КРАЈ)
        else:
            досад = ''.join([т.текст for т in бре.излаз])
            raise Exception(f'Лош цитат: ({бре.и.тренутно}), линија={бре.и.линија}, текст={досад[-100:]}')

